/*
Encapsulation - Bir klasstyn ozgocholuktorun(properties) 
  jana methodtorun(atkargan function) toluk jazyp koisok, 
  bir kapsula bolup kalat. Al Encapsulation dep atalat.
*/

/*
Inheritance - Murastoo(muras aluu). Bir class bashka bir class'tyn 
  methodtorun jana ozgocholuktorun(properties) muras katary importtop
  aluusu Inheritance dep atatlat

  `extends` keyword arkyluu muras alat
  `super` keyword parent(muras alyngan class)'tyn properties;terin ala alabyz
  `@override` keyword arkyluu parent class'tyn methodun ozgorto alabyz
*/

/*
Polymorphism - Kop turduuluk. Biz bir ele classka taandyk birok bashka 
  class tuzgubuz kelgende koldonobuz.

  M: Adam degen class bar. Oshel ele ushurda Bobok, Jash Adam je KaryganAdam boluusu mumkun
     Alardyn methodtoru je kee bir ozgocholuktory bashka-bashka bolup kalyshy mumkun.
*/